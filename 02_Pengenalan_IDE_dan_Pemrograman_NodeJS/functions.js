// Named Function
function perkalian(a, b) {
  let result = a * b;
  return result;
}

// Arrow Function
// Anonymous Function
const pertambahan = (a, b) => {
  let result = a + b;
  return result;
}

// Named Expression Function
const pembagian = function bagi(a, b) {
  let result = a / b;
  return result;
  
}

console.log(perkalian(5, 2));
console.log(pertambahan(6, 2));
console.log(pembagian(8, 2));


